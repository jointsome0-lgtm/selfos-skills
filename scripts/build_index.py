#!/usr/bin/env python3
"""Build generated AGENTS.md and README catalog surfaces from canonical skills/."""

from __future__ import annotations

import argparse
import difflib
import json
import os
from pathlib import Path
import re
import stat
import sys
import tempfile

from skill_catalog import (
    ROOT,
    derive_adapter_version,
    discover_skills,
    display_path,
    load_bundle_manifest,
)

AGENTS_PATH = ROOT / "AGENTS.md"
README_PATH = ROOT / "README.md"
ADAPTER_MANIFEST_PATHS = (
    ROOT / ".claude-plugin" / "plugin.json",
    ROOT / ".codex-plugin" / "plugin.json",
)
MANIFEST_VERSION_RE = re.compile(
    r'^(?P<prefix>\s*"version"\s*:\s*)"(?P<version>[^"\r\n]*)"(?P<suffix>\s*,?\s*)$',
    re.MULTILINE,
)
README_COMPATIBILITY_START = "<!-- BEGIN GENERATED COMPATIBILITY; do not edit by hand. -->"
README_COMPATIBILITY_END = "<!-- END GENERATED COMPATIBILITY -->"
README_INSERT_BEFORE = "\n## Repository layout\n"
LEGACY_COMPATIBILITY_NOTE = """`watch` requires Bash, Git, `gh`, `jq`, network access, and a configured open PR. `sdd-conventions` requires Python 3.9+ for its helper scripts. Other skills are Markdown-first and use only capabilities explicitly available in the host environment.

"""
PREAMBLE = """# selfos-skills — Portable Agent Skills Catalog

The installable source of truth is `skills/<name>/`, following the open Agent Skills format. Each skill's `metadata.selfos.version` is canonical, and every change in that installable tree requires a strict semantic-version increase. Bundled dependency copies are generated from each composed skill's `BUNDLE.json` manifest, self-identify through a `GENERATED.md` marker, and retain their source skill's version, while the containing composed skill is bumped when its regenerated tree changes. Both host adapter versions are generated by separately summing the catalog's major, minor, and patch components; substantive aggregate-adapter changes require packaging bumps for every skill. Installers and native plugin systems discover those folders directly; this file is only a generated catalog and a fallback for agents that can read repository instructions but do not yet support skills.

When working inside this repository, read a matching skill's `SKILL.md` in full and resolve every relative path from that skill folder. Rows marked **explicit only** must not auto-activate from a topical match. This repository is public: use invented examples only and never commit personal data, credentials, private paths, or local agent state.

"""

LEGACY_SECTION = """## Legacy plugin deprecation

The six Claude domain packages under `plugins/` were deprecated on {deprecated_on}; removal may proceed on or after {earliest_removal}. Both dates derive from `plugins/deprecation.json`; window history lives in [issue #66](https://github.com/jointsome0-lgtm/selfos-skills/issues/66). Their payloads are frozen snapshots: new behavior belongs only in `skills/`. A maintainer-applied `legacy-plugin-compatibility` or `legacy-plugin-security` label is required for an exceptional package-scoped fix, and every such fix requires a strict legacy manifest-version increase. The all-at-once removal uses `legacy-plugin-removal`.

Removal is blocked until:

- `selfos`, `atlas`, `exp2res`, `tollgate`, and `story` no longer document or depend on the legacy packages;
- documentation points users to `npx skills add jointsome0-lgtm/selfos-skills` or `selfos-skills@selfos`;
- the canonical installation smoke matrix is green on the removal commit; and
- a dedicated major-migration release note names every removed package and explains the cache transition.

"""

TRAILER = """<!-- Generated by scripts/build_index.py; do not edit by hand. -->
"""


def escape(value: str) -> str:
    return value.replace("|", "\\|")


def render_legacy_section() -> tuple[str, list[str]]:
    """Derive the legacy notice from the policy so the dates cannot go stale.

    A missing policy file means the legacy tree is gone and the section with it.
    """
    policy_path = ROOT / "plugins" / "deprecation.json"
    try:
        raw = policy_path.read_text(encoding="utf-8")
    except FileNotFoundError:
        return "", []
    except (OSError, UnicodeError) as exc:
        return "", [f"plugins/deprecation.json: cannot read UTF-8: {exc}"]
    try:
        policy = json.loads(raw)
    except json.JSONDecodeError as exc:
        return "", [f"plugins/deprecation.json: invalid JSON: {exc.msg}"]
    deprecated_on = policy.get("deprecated_on") if isinstance(policy, dict) else None
    earliest = policy.get("earliest_removal") if isinstance(policy, dict) else None
    if not isinstance(deprecated_on, str) or not isinstance(earliest, str):
        return "", [
            "plugins/deprecation.json: deprecated_on and earliest_removal must be strings"
        ]
    return LEGACY_SECTION.format(deprecated_on=deprecated_on, earliest_removal=earliest), []


def render() -> tuple[str | None, list[str]]:
    skills, errors = discover_skills()
    if errors:
        return None, errors
    rows = [
        "| Name | Version | When to use | Invocation | Bundled references | Path |",
        "| --- | --- | --- | --- | --- | --- |",
    ]
    for skill in skills:
        invocation = "**explicit only**" if skill.explicit_only else "automatic or explicit"
        dependencies, bundle_errors = load_bundle_manifest(skill.root)
        errors.extend(bundle_errors)
        bundled = ", ".join(f"`{name}`" for name in dependencies or ()) or "—"
        path = display_path(skill.path)
        rows.append(
            f"| `{skill.name}` | `{skill.version or 'invalid'}` | {escape(skill.description)} | "
            f"{invocation} | {bundled} | [{path}]({path}) |"
        )
    if errors:
        return None, errors
    legacy, legacy_errors = render_legacy_section()
    if legacy_errors:
        return None, legacy_errors
    return PREAMBLE + legacy + TRAILER + "\n".join(rows) + "\n", []


def render_readme_compatibility() -> tuple[str | None, list[str]]:
    skills, errors = discover_skills()
    if errors:
        return None, errors
    rows = [
        "| Skill | Version | Runtime compatibility |",
        "| --- | --- | --- |",
    ]
    for skill in skills:
        compatibility = skill.fields.get("compatibility")
        if compatibility is None:
            errors.append(
                f"{display_path(skill.path)}: frontmatter is missing required field 'compatibility'"
            )
            continue
        rows.append(f"| `{skill.name}` | `{skill.version or 'invalid'}` | {escape(compatibility)} |")
    if errors:
        return None, errors
    block = "\n".join(
        [
            README_COMPATIBILITY_START,
            "## Compatibility",
            "",
            "Compatibility describes hard runtime needs and conditional capabilities; descriptive host affinity in a skill's body is not lock-in.",
            "",
            *rows,
            README_COMPATIBILITY_END,
        ]
    )
    return block + "\n", []


def updated_readme(actual: str, block: str) -> tuple[str | None, list[str]]:
    start_count = actual.count(README_COMPATIBILITY_START)
    end_count = actual.count(README_COMPATIBILITY_END)
    if start_count == 1 and end_count == 1:
        start = actual.index(README_COMPATIBILITY_START)
        try:
            end = actual.index(README_COMPATIBILITY_END, start) + len(README_COMPATIBILITY_END)
        except ValueError:
            return None, ["README.md generated compatibility markers are out of order"]
        if end < len(actual) and actual[end] == "\n":
            end += 1
        return actual[:start] + block + actual[end:], []
    if start_count or end_count:
        return None, ["README.md has incomplete or duplicate generated compatibility markers"]
    if README_INSERT_BEFORE not in actual:
        return None, ["README.md is missing the Repository layout insertion anchor"]
    without_legacy = actual.replace(LEGACY_COMPATIBILITY_NOTE, "", 1)
    return without_legacy.replace(README_INSERT_BEFORE, f"\n{block}{README_INSERT_BEFORE}", 1), []


def show_diff(actual: str, expected: str, label: str) -> None:
    for line in difflib.unified_diff(
        actual.splitlines(),
        expected.splitlines(),
        fromfile=f"{label} (current)",
        tofile=f"{label} (generated)",
        lineterm="",
    ):
        print(line, file=sys.stderr)


def write_atomically(path: Path, content: str) -> None:
    mode = stat.S_IMODE(path.stat().st_mode) if path.exists() else 0o644
    descriptor, temporary_name = tempfile.mkstemp(prefix=f".{path.name}.", dir=path.parent)
    temporary = Path(temporary_name)
    try:
        with os.fdopen(descriptor, "w", encoding="utf-8", newline="\n") as handle:
            handle.write(content)
        os.chmod(temporary, mode)
        os.replace(temporary, path)
    except BaseException:
        temporary.unlink(missing_ok=True)
        raise


def rendered_adapter_manifest(path: Path, version: str) -> tuple[str | None, list[str]]:
    try:
        actual = path.read_text(encoding="utf-8")
        value = json.loads(actual)
    except (OSError, UnicodeError, json.JSONDecodeError) as exc:
        return None, [f"{display_path(path)}: cannot generate from invalid JSON: {exc}"]
    if not isinstance(value, dict):
        return None, [f"{display_path(path)}: top-level JSON value must be an object"]
    matches = list(MANIFEST_VERSION_RE.finditer(actual))
    if len(matches) != 1:
        return None, [
            f"{display_path(path)}: expected exactly one single-line string version field"
        ]
    match = matches[0]
    expected = (
        actual[: match.start()]
        + match.group("prefix")
        + json.dumps(version)
        + match.group("suffix")
        + actual[match.end() :]
    )
    return expected, []


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check", action="store_true", help="fail if AGENTS.md or README.md is stale"
    )
    args = parser.parse_args()
    expected_agents, errors = render()
    compatibility_block, compatibility_errors = render_readme_compatibility()
    errors.extend(compatibility_errors)
    skills, discovery_errors = discover_skills()
    errors.extend(discovery_errors)
    adapter_version, version_errors = derive_adapter_version(skills)
    errors.extend(version_errors)
    if errors or expected_agents is None or compatibility_block is None or adapter_version is None:
        for error in errors:
            print(f"ERROR: {error}", file=sys.stderr)
        return 1

    try:
        actual_agents = AGENTS_PATH.read_text(encoding="utf-8")
    except FileNotFoundError:
        actual_agents = ""
    except (OSError, UnicodeError) as exc:
        print(f"ERROR: cannot read AGENTS.md: {exc}", file=sys.stderr)
        return 1

    try:
        actual_readme = README_PATH.read_text(encoding="utf-8")
    except (OSError, UnicodeError) as exc:
        print(f"ERROR: cannot read README.md: {exc}", file=sys.stderr)
        return 1
    expected_readme, readme_errors = updated_readme(actual_readme, compatibility_block)
    if readme_errors or expected_readme is None:
        for error in readme_errors:
            print(f"ERROR: {error}", file=sys.stderr)
        return 1

    expected_adapters: list[tuple[Path, str, str]] = []
    for path in ADAPTER_MANIFEST_PATHS:
        try:
            actual = path.read_text(encoding="utf-8")
        except (OSError, UnicodeError) as exc:
            print(f"ERROR: cannot read {display_path(path)}: {exc}", file=sys.stderr)
            return 1
        expected, adapter_errors = rendered_adapter_manifest(path, adapter_version)
        if adapter_errors or expected is None:
            for error in adapter_errors:
                print(f"ERROR: {error}", file=sys.stderr)
            return 1
        expected_adapters.append((path, actual, expected))

    stale_agents = actual_agents != expected_agents
    stale_readme = actual_readme != expected_readme
    if not stale_agents:
        print("AGENTS.md is up to date.")
    elif args.check:
        print("ERROR: AGENTS.md is stale; run python scripts/build_index.py", file=sys.stderr)
        show_diff(actual_agents, expected_agents, "AGENTS.md")
    else:
        write_atomically(AGENTS_PATH, expected_agents)
        print("Wrote AGENTS.md.")

    if not stale_readme:
        print("README.md compatibility table is up to date.")
    elif args.check:
        print("ERROR: README.md is stale; run python scripts/build_index.py", file=sys.stderr)
        show_diff(actual_readme, expected_readme, "README.md")
    else:
        write_atomically(README_PATH, expected_readme)
        print("Wrote README.md compatibility table.")

    stale_adapter = False
    for path, actual, expected in expected_adapters:
        if actual == expected:
            print(f"{display_path(path)} derived version is up to date.")
            continue
        stale_adapter = True
        if args.check:
            print(
                f"ERROR: {display_path(path)} is stale; run python scripts/build_index.py",
                file=sys.stderr,
            )
            show_diff(actual, expected, display_path(path))
        else:
            write_atomically(path, expected)
            print(f"Wrote derived version {adapter_version} to {display_path(path)}.")

    return int(args.check and (stale_agents or stale_readme or stale_adapter))


if __name__ == "__main__":
    raise SystemExit(main())
